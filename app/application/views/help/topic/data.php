<h2>Adatok megadása</h2>

<p>Első lépésként az iskola és az iskolában dolgozók adatait kell bevinni a rendszerbe amikkel a későbbiekben dolgozni fogunk.
    A jövőben bármikor lehet ezeket módosítani. Amíg nincs létrehozva a csengetési rend az üres órarend nem jelenik meg.
    <br>A következőket kell megadni:
</p>

<ol>
    <li>Csöngetési rend, azaz az órák kezdete és vége</li>
    <li>Tantárgyak nevei</li>
    <li>Tanárok névsora, tanított tárgyai, maximum heti óraszáma</li>
    <li>Tantermek, benne tanított tantárgy ha van, férőhely</li>
    <li>Osztályok, osztályfőnök neve, osztályterem, létszám</li>
</ol>

<h4>Adatok kezelése</h4>

<p>Az adatok táblázat szerűen jelennek meg.<br>
    Mikor úgy sort szeretnénk hozzáadni a táblázathoz, válasszuk a
    <a href="#" title="Új sor beszúrása"><img src="http://localhost/assets/img/icons/edit.png" alt="Beszúrás">Beszúrás</a> lehetőséget.<br>
    Mikor egy elemet törlni szeretnénk az adott sor végén kattintsunk a
    <a href="#" title="Eltávolítás"><img src="http://localhost/assets/img/icons/drop.png" alt="Eltávolítás"></a> ikonra.<br>
    Lehetőségünk van egy excel táblázatba kimenteni a táblázatot, ehhez kattintsunk a
    <a href="#" title="Exportálás Excel táblázatba"><img src="http://localhost/assets/img/icons/export.png" alt="Exportálás">Exportálás</a> gombra.<br>
    Tanárok és termek megadásánál előfordulhat, hogy egy névhez több tantárgyat szeretnénk rendelni, ezt a
    <a href="#" title="Új tantárgy beszúrása"><img src="http://localhost/assets/img/icons/insert.png" alt="Új tantárgy beszúrása"></a> ikonra kattintva tudjuk megtenni.
</p>

<div class="highlight">
    <div class="warning"><i class="fas fa-exclamation-triangle mr-2"></i>Figyelmeztetés!</div>
    <p>A megadott adatok függenek egymástól. Tehát amíg nem létezik egy tantárgy a tanárok nem taníthatnak olyat, amíg nem létezik a tanterem, nem lehet osztályteremnek választani. A többi kapcsolat is ezen logika alapján működik. Ha törlönk egy elemet, amire építettünk az mindenhonnan tölődik a rendszerből.</p>
