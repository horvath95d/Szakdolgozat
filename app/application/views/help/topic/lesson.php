<h2>Tanórák</h2>

<p>A tanórák menüpont alatt lehet az órarendben megjelenő órákat létrehozni. Itt az előzőleg megadott adatok alapján tudjuk összeálítani az órákat. A bal oldali navigációs sávban kell kiválasztani, h kinek az órarendjéhez akarunk órát adni.</p>

<h4>Egy osztálynak óra</h4>
<p>Osztály nevére kattintva megjelennek az osztály órái.</p>

<h4>Óra egy évfolyamnak</h4>
<p>Az évfolyam számát választa olyan órákat hozhat létre, amiket az adott évfolyam összes osztálya lát az órarendjében</p>

<h4>Tanári lyukasóra</h4>
<p>Ezzel a lehetőséggel olyan órákat hozhat létre, amikkel megjelölhet olyan időpontokat amikor az adott tanár nem tanít. Lyukas órát csak akkor van értelme létrehozni, ha fix időpontot adunk neki. Ezek az órák csak akkor látszanak az órarendbe, ha csak a tanárra szűrünk.</p>

<h4>Összevont óra</h4>
<p>Több osztálynak vagy évfolyamnak ugyan az a tantárgy, tanár, terem és időpont.</p>

<h4>Bontott óra</h4>
<p>Egy osztálynak egy időpontban több órája van. Pl.: Angol/Német csoport bontás.</p>